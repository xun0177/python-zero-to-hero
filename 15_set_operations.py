"""
第15题：集合运算
知识点：集合的定义、交集（&）、并集（|）、差集（-）、对称差集（^）
"""

def set_operations_demo():  # 函数名清晰表达"集合运算演示"的功能
    # ---------- 定义两个集合 ----------
    # 集合用花括号 {} 定义，元素不重复且无序
    set1 = {1, 2, 3, 4, 5, 7, 9}  # 注意：7 和 9 只在 set1 中
    set2 = {1, 2, 3, 4, 5, 6, 8}  # 注意：6 和 8 只在 set2 中

    # ---------- 打印原始集合 ----------
    print(f'集合 set1 = {set1}')
    print(f'集合 set2 = {set2}')
    print()  # 输出空行，让结果更清晰

    # ---------- 交集（&）：同时属于两个集合的元素 ----------
    # 在 set1 中且在 set2 中的元素
    intersection = set1 & set2
    print(f'交集（同时属于两个集合的元素）：{intersection}')

    # ---------- 并集（|）：属于至少一个集合的元素 ----------
    # 在 set1 中或在 set2 中的元素（合并后去重）
    union = set1 | set2
    print(f'并集（属于至少一个集合的元素）：{union}')

    # ---------- 差集（-）：属于 set1 但不属于 set2 的元素 ----------
    # 注意：set1 - set2 和 set2 - set1 结果不同
    difference = set1 - set2
    print(f'差集（属于 set1 但不属于 set2 的元素）：{difference}')

    # ---------- 对称差集（^）：属于 set1 或 set2 但不同时属于两者的元素 ----------
    # 即：并集减去交集
    symmetric_difference = set1 ^ set2
    print(f'对称差集（只属于其中一个集合的元素）：{symmetric_difference}')

# 调用函数执行演示
set_operations_demo()


