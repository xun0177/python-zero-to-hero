"""
第4题：条件判断（if ... elif ... else）
知识点：多分支结构、for 循环遍历列表、函数参数传递
"""

# 准备测试数据：包含负数、零、正数，覆盖所有分支情况
input_nums = [-2, -1, 0, 1, 2]  # 列表类型，用方括号包裹，逗号分隔

def classify_number(nums):  # 函数名直观表达“对数字进行分类”的功能
    """遍历 nums 中的每个数字，判断正负或零并打印"""
    for num in nums:  # for 循环依次取出列表中的每个元素
        if num < 0:   # 条件1：数字小于 0
            print(f'{num}是负数')
        elif num == 0:  # 条件2：等于 0（仅当条件1为 False 时检查）
            print(f'{num}是零')
        else:  # 条件3：以上都不满足（即数字大于 0）
            print(f'{num}是正数')

# 调用函数，将 input_nums 作为实参传入（实参与形参名称可以不同）
classify_number(input_nums)


"""
补充说明

1. if 语句的执行顺序：
   - 从上到下依次判断条件。
   - 一旦某个条件为 True，执行对应的代码块，然后跳过整个 if 结构。

2. elif 与 else 的作用：
   - elif 是 "else if" 的缩写，可以有任意多个。
   - else 不需要条件，是“前面所有条件都不成立”时的最终出口。

3. 列表与 for 循环：
   - 列表（list）用方括号定义，可以存放多个元素。
   - for 循环可以遍历列表中的每一个元素。

4. 形参与实参：
   - 定义函数时括号内的 nums 是形参（形式参数）。
   - 调用时传入的 input_nums 是实参（实际参数）。
   - 两者名称可以不同，本例特意用不同名称展示其隔离性。

5. 数字在布尔上下文中的表现：
   - 0 被视为 False，非零数字被视为 True。
   - 但本题显式写 num == 0 比写 if not num 更清晰，推荐这样做。

6. 扩展思考：
   将列表元素改为浮点数（如 -2.5, 0, 1.5），代码依然正常工作。
"""