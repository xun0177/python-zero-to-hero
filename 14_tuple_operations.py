import math  # 导入数学模块，用于开方计算

def tuple_operations_demo():  # 函数名清晰表达"元组操作演示"的功能
    # ---------- 二维坐标点（元组） ----------
    point_a = (2, 2)  # 元组用圆括号定义，元素用逗号分隔
    print(f'点A的坐标为：{point_a}')

    # 元组解包：将元组中的元素依次赋值给多个变量
    # 变量的数量必须与元组元素数量一致，否则会报错
    x_a, y_a = point_a
    print(f'点A的横坐标为：{x_a}，纵坐标为：{y_a}')

    # 计算点 A 到原点 (0, 0) 的距离（欧几里得距离）
    # 公式：√(x² + y²)，使用 math.sqrt() 开平方
    distance_a = math.sqrt(x_a**2 + y_a**2)
    print(f'点A到原点的距离为：{distance_a:.2f}')  # 保留两位小数

    # ---------- 三维坐标点（元组） ----------
    point_b = (3, 3, 3)  # 三维坐标，元组包含三个元素
    print(f'点B的坐标为：{point_b}')

    # 三元组解包：一次性解包三个元素
    x_b, y_b, z_b = point_b
    print(f'点B的横坐标为：{x_b}，纵坐标为：{y_b}，竖坐标为：{z_b}')

    # 计算点 B 到原点的距离
    # 通过索引访问元组元素（索引从 0 开始）
    distance_b = math.sqrt(point_b[0]**2 + point_b[1]**2 + point_b[2]**2)
    print(f'点B到原点的距离为：{distance_b:.2f}')

    # ---------- 遍历点坐标列表 ----------
    points = [(1, 2), (3, 4), (5, 6)]  # 列表中嵌套元组，每个元素是一个二维坐标点

    # enumerate(可迭代对象, start=1) 同时获取索引和值，索引从 1 开始
    for idx, point in enumerate(points, 1):
        print(f'点{idx}的坐标为：{point}')

# 调用函数执行演示
tuple_operations_demo()


