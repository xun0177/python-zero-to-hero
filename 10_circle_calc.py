"""
第10题：圆的周长与面积计算
知识点：导入 math 模块、math.pi 常量、圆的周长和面积公式、格式化输出保留小数
"""

import math  # 导入数学模块，提供数学常量和函数

def calculate_circle(radius):  # 函数名清晰表达"计算圆"的功能，参数名 radius 明确表示"半径"
    """计算并打印给定半径的圆的周长和面积"""
    # 周长公式：C = 2πr
    circumference = 2 * math.pi * radius  # math.pi 是圆周率常量（约 3.141592653589793）
    
    # 面积公式：S = πr²
    area = math.pi * radius * radius  # r * r 即半径的平方
    
    # f"{变量:.2f}" 表示保留两位小数输出（四舍五入）
    print(f'周长为：{circumference:.2f}')  # 输出周长，保留两位小数
    print(f'面积为：{area:.2f}')          # 输出面积，保留两位小数

# 调用函数，计算半径为 8 的圆的周长和面积
calculate_circle(8)


