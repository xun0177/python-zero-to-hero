"""
第8题：字典的增删改查与遍历
知识点：字典的定义、添加键值对、修改值、遍历字典、列表作为字典的值
"""

def student_info_operations():  # 函数名直观表达“学生信息操作”的功能
    # ---------- 创建字典 ----------
    student = {
        "name": "我",           # 键值对用冒号分隔，多个键值对用逗号分隔
        "age": 18,
        "subject": "Python",
        "score": [95, 96, 97, 98, 99]  # 值可以是任意类型，这里嵌套了一个列表
    }

    # ---------- 增（添加新键值对） ----------
    student["ID"] = "001"  # 直接赋值给一个新键，如果键不存在则会新增

    # ---------- 改（修改已有键的值） ----------
    student['age'] = 19  # 直接赋值给已有键，会覆盖原来的值

    # ---------- 查（遍历字典） ----------
    # items() 方法返回包含所有键值对的视图，可同时遍历键和值
    for key, value in student.items():
        print(key, ":", value)  # 打印每个键和对应的值

    # ---------- 计算成绩的平均分 ----------
    # sum() 计算列表元素总和，len() 计算列表长度（元素个数）
    print(f'avg_score：{sum(student['score']) / len(student['score'])}')

# 调用函数执行演示
student_info_operations()


"""
补充说明

1. 字典的特点：
   - 用花括号 {} 定义，键和值用冒号分隔。
   - 键必须是不可变类型（如字符串、数字、元组），值可以是任意类型。
   - 字典是无序的（Python 3.7 之后按插入顺序排列）。

2. 字典的增和改：
   - 字典[键] = 值：如果键存在则修改，不存在则新增。
   - 也可以使用 update() 方法批量更新。

3. 字典的删除（本例未涉及）：
   - del 字典[键] 删除指定键值对。
   - pop(键) 删除并返回对应的值。
   - clear() 清空整个字典。

4. items() 遍历方法：
   - for key, value in 字典.items(): 同时获取键和值。
   - 也可以用 keys() 单独遍历键，values() 单独遍历值。

5. sum() 和 len() 函数：
   - sum(列表) 计算列表中所有元素的和。
   - len(列表) 返回列表的元素个数。
   - 两者结合可以快速计算平均值。

6. 扩展思考：
   如果字典中嵌套了列表，直接修改列表中的元素会影响原字典，因为列表是可变对象。
"""