"""
第3题：输入与类型转换
知识点：input() 获取用户输入、int() 类型转换、字符串拼接与格式化
"""

def sum_num():  # 定义函数，实现两数求和功能
    # input() 会暂停程序，等待用户输入并按回车，返回值永远是字符串类型
    num1 = input("输入第一个整数：")   # 提示语显示在控制台
    num2 = input("输入第二个整数：")   # 用户输入的内容以字符串形式存储

    # int() 将字符串转换为整数，转换后才能进行数学加法运算
    # 如果用户输入的是字母或小数，程序会报错（触发 ValueError）
    print(f'两数之和为：{int(num1) + int(num2)}')  

# 调用函数，触发执行
sum_num()


"""
补充说明

1. input() 的特性：
   - 无论用户输入什么内容，返回的都是字符串（str）类型。
   - 括号内的字符串会作为提示语显示在控制台。

2. 类型转换（Type Conversion）：
   - int(变量) 将内容转换为整数类型。
   - 常见的还有 float()（转浮点数）、str()（转字符串）。

3. 字符串拼接与格式化：
   本例用 f-string 将计算结果嵌入到字符串中，
   也可以写成 print('两数之和为：' + str(int(num1) + int(num2)))

4. 潜在错误（预习）：
   如果用户输入字母（如 abc），int() 会报错，这是后面异常处理章节要解决的问题。
"""