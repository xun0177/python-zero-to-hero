"""
第1题：函数定义与调用
知识点：函数的定义（def）、函数的调用、缩进表示代码块
"""

def prints():  # def 是关键字，用于定义函数；prints 是函数名
    # 函数体内部缩进的代码属于这个函数
    print('Hello, world!')  # 调用内置 print 函数，在控制台输出字符串
    print('你好，调用这个函数，就会执行函数的内容。')  # 函数体可以包含多条语句

# 函数定义完成后不会自动执行，必须显式调用
prints()  # 调用函数，程序跳转到函数内部执行所有语句


"""
补充说明

1. 函数的定义格式：
   def 函数名():
       函数体（缩进的代码块）

2. 函数的调用：
   函数名() 表示执行该函数，调用时才会真正运行函数内的代码。

3. 缩进的重要性：
   Python 使用缩进（通常是 4 个空格或 1 个 Tab）来表示代码的层级关系，
   函数体必须比 def 语句多一层缩进。
"""